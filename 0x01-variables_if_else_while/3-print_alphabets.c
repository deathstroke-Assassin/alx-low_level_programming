#include <stdio.h>
#include <time.h>
#include <stdlib.h>
/**
 * main - assigns a random number to int n everytime
 * it executes, and prints it
 * Return: Always 0 (Success)
 */
int main(void)
{
char chr;
char CHR;
for (chr = 'a'; chr <= 'z'; chr++)
{
putchar (chr);
}
for (CHR = 'A'; CHR <= 'Z'; CHR++)
{
putchar (CHR);
}
putchar ('\n');
return (0);
}

