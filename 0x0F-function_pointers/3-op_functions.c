#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - add
 * @a: a
 * @b: b
 * Return: res
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - s
 * @a: a
 * @b: b
 * Return: res
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_div - add
 * @a: a
 * @b: b
 * Return: res
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mul - add
 * @a: a
 * @b: b
 * Return: res
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_mod - add
 * @a: a
 * @b: b
 * Return: res
 */
int op_mod(int a, int b)
{
	return (a % b);
}
